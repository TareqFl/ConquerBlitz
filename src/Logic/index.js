export function conquer({
  Game,
  column,
  row,
  direction,
  owned,
  backgroundColor,
  func = (newBoard) => newBoard,
}) {
  function conquer_East() {
    if (Game[column].rows[row + 1]) {
      let newBoard = Game;
      let captured = newBoard[column].rows[row + 1];
      let ownership = Game[column].rows[row].owned;
      captured.owned = ownership;
      captured.backgroundColor = backgroundColor;
      return func(newBoard);
    }
  }

  function conquer_West() {
    if (Game[column].rows[row - 1]) {
      let newBoard = Game;
      let captured = newBoard[column].rows[row - 1];
      let ownership = Game[column].rows[row].owned;
      captured.owned = ownership;
      captured.backgroundColor = backgroundColor;
      return func(newBoard);
    }
  }
  function conquer_South() {
    if (Game[column + 1]) {
      let newBoard = Game;
      let captured = newBoard[column + 1].rows[row];
      let ownership = Game[column].rows[row].owned;
      captured.owned = ownership;
      captured.backgroundColor = backgroundColor;
      return func(newBoard);
    }
  }
  function conquer_North() {
    if (Game[column - 1]) {
      let newBoard = Game;
      let captured = newBoard[column - 1].rows[row];
      let ownership = Game[column].rows[row].owned;
      captured.owned = ownership;
      captured.backgroundColor = backgroundColor;
      return func(newBoard);
    }
  }

  if (owned !== null) {
    switch (direction) {
      case 'east':
        return conquer_East();
      case 'south':
        return conquer_South();
      case 'west':
        return conquer_West();
      case 'north':
        return conquer_North();
      default:
        return;
    }
  }

  // End of conquer
}
